#!/usr/bin/env python3
first_num = int(input("Give me the first number: "))
second_num = int(input("Give me the second number: "))

print("Thank you!")

print(str(first_num) + " + " + str(second_num) + " = " + str(first_num + second_num))
print(str(first_num) + " - " + str(second_num) + " = " + str(first_num - second_num))
print(str(first_num) + " / " + str(second_num) + " = " + str(first_num / second_num))
print(str(first_num) + " * " + str(second_num) + " = " + str(first_num * second_num))